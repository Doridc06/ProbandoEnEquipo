package ejer1;
public class Probando {
    private int num1;
    private int num2;
    public Probando(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    public static int suma(int num1, int num2){
        int suma = num1 + num2;
        return suma;
    }

    public static int resta(int num1, int num2){
        int resta = num1 - num2;
        return resta;
    }
}
